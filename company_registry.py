def make_slug(company_name: str) -> str:
    """Return a deterministic, ASCII-only, Windows-safe slug for *company_name*.

    Uses python-slugify with allow_unicode=False to force ASCII transliteration.
    This converts accented characters (á→a, ñ→n, ü→u, etc.) and strips all
    characters that are unsafe on Windows NTFS (parentheses, dots, slashes, etc.).

    Examples
    --------
    >>> make_slug("Clínica Las Américas")
    'clinica-las-americas'
    >>> make_slug("EPS Sánitas (NUEVA)")
    'eps-sanitas-nueva'
    >>> make_slug("Organización Sanitas S.A.")
    'organizacion-sanitas-s-a'

    WARNING: Do NOT use allow_unicode=True here. Unicode directory names can
    cause encoding issues on Windows NTFS and are not portable across OS locales.
    See RESEARCH.md Pitfall 4.
    """
    return slugify(company_name, allow_unicode=False, separator="-")


def make_storage_path(base_dir: Path, country: str, slug: str) -> Path:
    """Create and return the storage directory for a LATAM company.

    Directory structure: base_dir / "latam" / country.lower() / slug

    This mirrors the US pipeline layout (data/clean/{ticker}/) so that
    dashboard code reading data/latam/{country}/{slug}/financials.parquet
    uses the same logic as data/clean/AAPL/financials.parquet.

    Parameters
    ----------
    base_dir : Path
        Project data root (typically Path("data")).
    country : str
        Country name — will be lowercased (e.g. "Colombia" → "colombia").
    slug : str
        Company slug from make_slug().

    Returns
    -------
    Path
        The created directory path.
    """
    path = base_dir / "latam" / country.lower() / slug
    path.mkdir(parents=True, exist_ok=True)
    return path


def write_meta_json(path: Path, record: CompanyRecord) -> None:
    """Write meta.json to *path* / "meta.json" from a CompanyRecord.

    The JSON schema is defined in RESEARCH.md Pattern 3 and includes all
    CompanyRecord fields plus pipeline tracking fields (last_scraped,
    last_processed, fiscal_years_available, extraction_quality, etc.).

    Parameters
    ----------
    path : Path
        Directory where meta.json will be written (the company's storage dir).
    record : CompanyRecord
        The company metadata to serialise.

    Notes
    -----
    ensure_ascii=False preserves Unicode in display names (company_name) so
    that meta.json is human-readable without escape sequences.
    """
    meta = {
        "company_name": record.company_name,
        "slug": record.slug,
        "country": record.country,
        "regulatory_id": record.regulatory_id,
        "regulatory_authority": record.regulatory_authority,
        "source_url": record.source_url,
        "currency_original": record.currency_original,
        "approximated_fx": record.approximated_fx,
        "low_confidence_fx": record.low_confidence_fx,
        "last_scraped": None,
        "last_processed": None,
        "fiscal_years_available": [],
        "extraction_quality": None,
        "fields_extracted": [],
        "fields_missing": [],
    }
    (path / "meta.json").write_text(
        json.dumps(meta, indent=2, ensure_ascii=False), encoding="utf-8"
    )
