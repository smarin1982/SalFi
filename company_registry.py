"""Company registry for the LATAM pipeline.

Provides:
  - CompanyRecord: dataclass representing a LATAM company
  - make_slug(): deterministic ASCII slug from Unicode company names
  - make_storage_path(): creates data/latam/{country}/{slug}/ directory
  - write_meta_json(): writes meta.json to a company's storage directory

The slug is the permanent filesystem key for every LATAM company. It must be
deterministic, Windows NTFS-safe, and generated from Unicode Spanish/Portuguese
names.

The storage layout mirrors the US pipeline schema (data/clean/{ticker}/) so
that existing dashboard code works without modification — LATAM companies live
at data/latam/{country}/{slug}/ instead.

References:
  - RESEARCH.md Pattern 2 (slug generation via python-slugify)
  - RESEARCH.md Pattern 4 (storage layout and meta.json schema)
"""

