# ---------------------------------------------------------------------------
# CompanyRecord dataclass
# ---------------------------------------------------------------------------

@dataclass
class CompanyRecord:
    """Metadata record for a single LATAM company.

    Fields
    ------
    company_name : str
        Full Unicode display name (e.g. "Clínica Las Américas").
    slug : str
        ASCII filesystem key generated by make_slug() (e.g. "clinica-las-americas").
    country : str
        Lowercase country name (e.g. "colombia", "peru").
    regulatory_id : str
        NIT / RUC / RUT identifier as published by the regulatory authority.
    regulatory_authority : str
        Authority that published the filing (e.g. "Supersalud", "SMV", "CMF").
    source_url : str
        URL of the source document or portal page.
    currency_original : str
        Three-letter ISO currency code as reported (e.g. "COP", "BRL", "ARS").
    approximated_fx : bool
        True when the FX rate used is an approximation (annual average).
    low_confidence_fx : bool
        True for ARS and other currencies with volatile/unofficial rates.
        See RESEARCH.md Pitfall 3 and v2.0 Roadmap FX decision.
    """

    company_name: str
    slug: str
    country: str
    regulatory_id: str
    regulatory_authority: str
    source_url: str
    currency_original: str
    approximated_fx: bool = field(default=False)
    low_confidence_fx: bool = field(default=False)


